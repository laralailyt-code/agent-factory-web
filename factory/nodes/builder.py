def _sparkline_svg(values, color):
    if not values:
        return ""
    w, h = 120, 32
    n = len(values)
    vmin, vmax = min(values), max(values)
    rng = max(vmax - vmin, 1)
    pts = " ".join(
        f"{i * w / max(n - 1, 1):.1f},{h - (v - vmin) / rng * h:.1f}"
        for i, v in enumerate(values)
    )
    return (f'<svg width="{w}" height="{h}" viewBox="0 0 {w} {h}">'
            f'<polyline fill="none" stroke="{color}" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round" points="{pts}"/>'
            f'</svg>')


