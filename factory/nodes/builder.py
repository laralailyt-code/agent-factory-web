from __future__ import annotations
import pandas as pd
from pathlib import Path
from typing import Tuple


def load_excel(path: Path) -> pd.DataFrame:
    """Read an .xlsx, handling merged cells gracefully (v0.1.1 patch)."""
    df = pd.read_excel(path, engine="openpyxl")
    df = df.ffill()  # fill merged cells downward
    return df


def diff_dataframes(
    old: pd.DataFrame, new: pd.DataFrame, key: str = "SKU",
) -> dict[str, pd.DataFrame]:
    """Return {added, removed, changed} relative to `key`."""
    old_idx = old.set_index(key)
    new_idx = new.set_index(key)
    added_keys = new_idx.index.difference(old_idx.index)
    removed_keys = old_idx.index.difference(new_idx.index)
    common = new_idx.index.intersection(old_idx.index)
    changed = new_idx.loc[common].compare(old_idx.loc[common])
    return {
        "added": new_idx.loc[added_keys].reset_index(),
        "removed": old_idx.loc[removed_keys].reset_index(),
        "changed": changed.reset_index() if not changed.empty else pd.DataFrame(),
    }


