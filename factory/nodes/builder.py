# ============ HELPERS ============

# D2.4 demo trigger — only used in mock mode, so we can show the self-fix loop
# without needing real API. Any of these keywords in user_request will cause
# Builder iteration 0 to inject a syntax error into main.py; iteration 1 fixes it.
_INJECT_FAILURE_KEYWORDS = ["故意", "intentional", "error demo", "import 錯誤", "壞 prompt"]


def _select_files_bundle(subcategory: str) -> dict[str, str]:
    """Pick the right mock bundle based on the PRD subcategory."""
    return MOCK_FILES_BY_CATEGORY.get(subcategory, MOCK_FILES_BY_CATEGORY["_default"])


def _should_inject_failure(user_request: str, iteration: int) -> bool:
    if iteration != 0:
        return False
    return any(k in user_request for k in _INJECT_FAILURE_KEYWORDS)


def _summarize_tool_call(name: str, inp: dict) -> str:
    if name == "write_file":
        return f"write_file {inp.get('path', '?')}"
    if name == "read_file":
        return f"read_file {inp.get('path', '?')}"
    if name == "list_dir":
        return f"list_dir {inp.get('path', '.')}"
    if name == "run_command":
        cmd = inp.get("cmd", "")
        return f"run_command {cmd[:60]}"
    if name == "submit_done":
        return "submit_done"
    return name


def _read_sandbox_files(job_id: str) -> dict[str, str]:
    """Read every text file in generated/{job_id}/ recursively (after agent loop)."""
    root = Path("generated") / job_id
    if not root.exists():
        return {}
    out: dict[str, str] = {}
    for p in root.rglob("*"):
        if not p.is_file():
            continue
        rel = p.relative_to(root).as_posix()
        try:
            out[rel] = p.read_text(encoding="utf-8")
        except (UnicodeDecodeError, OSError):
            continue
    return out


# ============ MOCK AGENT LOOP ============

def _builder_mock(state: FactoryState, job_id: str, iteration: int, log: list[str]) -> dict[str, str]:
    """Simulate the agent loop in mock mode: real tool calls via tools.py,
    but file contents come from MOCK_FILES_BY_CATEGORY."""
    subcat = state["prd"].get("subcategory", "_default")
    bundle = _select_files_bundle(subcat)
    file_plan = state["design"].get("file_plan") or list(bundle.keys())

    user_request = state.get("user_request", "")
    inject_fail = _should_inject_failure(user_request, iteration)

    written: dict[str, str] = {}
    tool_calls = 0

    for name in file_plan:
        content = bundle.get(name, f"# auto-generated stub for {name}\n")
        if inject_fail and name == "main.py":
            content = content + "\n# D2.4 demo: intentional syntax error\nthis is not valid python at all !!!\n"

        result = write_file(job_id, name, content)
        tool_calls += 1
        if result["ok"]:
            log.append(f"  [tool] write_file {name} ✓")
            written[name] = content
        else:
            log.append(f"  [tool] write_file {name} ✗ ({result['error']})")

    # one verification command — exercise run_command for real
    verify = run_command(job_id, "python --version")
    tool_calls += 1
    check = "✓" if verify["ok"] else "✗"
    log.append(f"  [tool] run_command python --version {check}")

    # signal done
    tool_calls += 1
    log.append(f"  [tool] submit_done ✓")

    log.append(f"✓ Builder: 寫完 {len(written)} 個檔案 · {tool_calls} 次工具呼叫")
    return written


# ============ REAL AGENT LOOP ============

def _builder_real(state: FactoryState, job_id: str, iteration: int, log: list[str]) -> dict[str, str]:
    """Real Claude agent loop with tool use."""
    prd = state["prd"]
    design = state["design"]

    user_msg = (
        f"PRD:\n{json.dumps(prd, ensure_ascii=False, indent=2)}\n\n"
        f"Design:\n{json.dumps(design, ensure_ascii=False, indent=2)}\n\n"
        f"請依 design.file_plan,把每個檔案用 write_file 寫到 sandbox。"
        f"全部寫完後呼叫 submit_done。"
    )

    if iteration > 0:
        prev_errors = state.get("test_results", {}).get("errors", [])
        if prev_errors:
            errors_str = "\n".join(prev_errors)
            user_msg += (
                f"\n\n⚠️ 上一次測試失敗了 (iteration {iteration - 1}),錯誤如下:\n"
                f"{errors_str}\n\n請修正後重新 write_file 把整個檔案重寫。"
            )

    client = _get_client()
    messages: list[dict] = [{"role": "user", "content": user_msg}]

    max_steps = 30
    tool_calls = 0

    for _step in range(max_steps):
        resp = client.messages.create(
            model=MODELS["sonnet"],
            max_tokens=4096,
            system=BUILDER_SYSTEM,
            tools=TOOLS_SCHEMA,
            messages=messages,
        )

        # Echo assistant content back into the conversation (required for tool loops).
        assistant_blocks: list[dict] = []
        for block in resp.content:
            if hasattr(block, "model_dump"):
                assistant_blocks.append(block.model_dump())
            else:
                assistant_blocks.append(block)
        messages.append({"role": "assistant", "content": assistant_blocks})

        if resp.stop_reason == "end_turn":
            break
        if resp.stop_reason != "tool_use":
            break

        tool_results: list[dict] = []
        finished = False

        for block in resp.content:
            if getattr(block, "type", None) != "tool_use":
                continue

            tool_name = block.name
            tool_input = dict(block.input) if block.input else {}
            tool_calls += 1

            result = execute_tool(tool_name, job_id, **tool_input)

            summary = _summarize_tool_call(tool_name, tool_input)
            check = "✓" if result["ok"] else "✗"
            log.append(f"  [tool] {summary} {check}")

            tool_results.append({
                "type": "tool_result",
                "tool_use_id": block.id,
                "content": (result["output"] or result["error"] or "ok")[:1500],
                "is_error": not result["ok"],
            })

            if tool_name == "submit_done":
                finished = True

        messages.append({"role": "user", "content": tool_results})

        if finished:
            break

    written = _read_sandbox_files(job_id)
    log.append(f"✓ Builder: 寫完 {len(written)} 個檔案 · {tool_calls} 次工具呼叫")
    return written


# ============ NODE ENTRYPOINT ============

def builder_node(state: FactoryState) -> FactoryState:
    log = state.get("log", [])
    job_id = state.get("job_id", "unknown")

    # D2.4: bump iteration counter on retry
    iteration = state.get("iteration", 0)
    is_retry = bool(state.get("test_results", {}).get("failed", 0))
    if is_retry:
        iteration = iteration + 1
        log.append(f"⚙️  Builder: 看到 error · 進入 retry iteration {iteration}")
    else:
        log.append("⚙️  Builder: 開始 agent loop...")

    if is_mock():
        files = _builder_mock(state, job_id, iteration, log)
    else:
        files = _builder_real(state, job_id, iteration, log)

    return {
        **state,
        "files": files,
        "iteration": iteration,
        "current_stage": "tester",
        "log": log,
    }
